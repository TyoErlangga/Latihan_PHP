echo "++x = " . ++$x . " (naik dulu baru tampil)<br/>";
echo "x++ = " . $x++ . " (tampil dulu jadi " . $x . ")<br/>";
echo "--x = " . --$x . " (turun dulu baru tampil)<br/>";
echo "x-- = " . $x-- . " (tampil dulu jadi " . $x . ")<br/>";
